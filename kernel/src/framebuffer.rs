// ── Pixel format ─────────────────────────────────────────────────────────────
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum PixelFormat {
    Rgb,    // 3 bytes: R G B
    Bgr,    // 3 bytes: B G R
    Bgrx,   // 4 bytes: B G R X (most common UEFI GOP)
    Unknown,
}

// ── Framebuffer handle ────────────────────────────────────────────────────────
pub struct Framebuffer {
    ptr:           *mut u8,
    width:         usize,
    height:        usize,
    stride:        usize,   // pixels per row (may differ from width due to alignment)
    bytes_per_pixel: usize,
    format:        PixelFormat,
}

unsafe impl Send for Framebuffer {}
unsafe impl Sync for Framebuffer {}

impl Framebuffer {
    /// Write one pixel, handling the pixel format.
    /// Hard-guards against writes past the last mapped framebuffer page:
    /// `height × stride × bpp` bytes is the exclusive upper bound.
    #[inline(always)]
    fn write_pixel_raw(&mut self, offset: usize, r: u8, g: u8, b: u8) {
        let limit = self.height * self.stride * self.bytes_per_pixel;
        if offset + self.bytes_per_pixel > limit { return; }
        unsafe {
            let p = self.ptr.add(offset);
            match self.format {
                PixelFormat::Rgb => {
                    p.write(r); p.add(1).write(g); p.add(2).write(b);
                }
                PixelFormat::Bgr => {
                    p.write(b); p.add(1).write(g); p.add(2).write(r);
                }
                PixelFormat::Bgrx | PixelFormat::Unknown => {
                    p.write(b); p.add(1).write(g); p.add(2).write(r); p.add(3).write(0xFF);
                }
            }
        }
    }

    pub fn put_pixel(&mut self, x: usize, y: usize, r: u8, g: u8, b: u8) {
        if x >= self.width || y >= self.height { return; }
        let off = (y * self.stride + x) * self.bytes_per_pixel;
        self.write_pixel_raw(off, r, g, b);
    }

    /// Read back a pixel from the linear framebuffer.
    pub fn get_pixel(&self, x: usize, y: usize) -> (u8, u8, u8) {
        if x >= self.width || y >= self.height { return (0, 0, 0); }
        let off = (y * self.stride + x) * self.bytes_per_pixel;
        unsafe {
            let p = self.ptr.add(off);
            match self.format {
                PixelFormat::Rgb => (*p, *p.add(1), *p.add(2)),
                PixelFormat::Bgr => (*p.add(2), *p.add(1), *p),
                PixelFormat::Bgrx | PixelFormat::Unknown => (*p.add(2), *p.add(1), *p),
            }
        }
    }

    pub fn fill_rect(&mut self, x: usize, y: usize, w: usize, h: usize, r: u8, g: u8, b: u8) {
        let x_end = (x + w).min(self.width);
        let y_end = (y + h).min(self.height);
        for py in y..y_end {
            for px in x..x_end {
                let off = (py * self.stride + px) * self.bytes_per_pixel;
                self.write_pixel_raw(off, r, g, b);
            }
        }
    }

    /// Blit a raw BGRA/BGRX pixel buffer (one `u32` per pixel, little-endian `0x00RRGGBB`).
    pub fn blit(&mut self, x: usize, y: usize, w: usize, h: usize, pixels: &[u32]) {
        for py in 0..h {
            for px in 0..w {
                let p = pixels[py * w + px];
                let b = ((p >> 16) & 0xFF) as u8;
                let g = ((p >>  8) & 0xFF) as u8;
                let r = ( p        & 0xFF) as u8;
                self.put_pixel(x + px, y + py, r, g, b);
            }
        }
    }

    pub fn clear(&mut self, r: u8, g: u8, b: u8) {
        self.fill_rect(0, 0, self.width, self.height, r, g, b);
    }

    pub fn width(&self) -> usize { self.width }
    pub fn height(&self) -> usize { self.height }

    // ── Text rendering ────────────────────────────────────────────────────────

    /// Draw a single ASCII character using the 8×16 built-in bitmap font.
    /// Returns the pixel x-offset after the character.
    pub fn draw_char(&mut self, x: usize, y: usize, ch: u8,
                     (fr, fg, fb): (u8, u8, u8),
                     (br, bg, bb): (u8, u8, u8)) -> usize {
        let glyph = FONT_8X16.glyph(ch);
        for row in 0..16usize {
            let bits = glyph[row];
            for col in 0..8usize {
                let lit = (bits >> (7 - col)) & 1 != 0;
                let (r, g, b) = if lit { (fr, fg, fb) } else { (br, bg, bb) };
                self.put_pixel(x + col, y + row, r, g, b);
            }
        }
        x + 8
    }

    /// Draw an ASCII string; wraps at screen edge.
    pub fn draw_str(&mut self, mut x: usize, y: usize, text: &str,
                    fg: (u8, u8, u8), bg: (u8, u8, u8)) -> usize {
        for &ch in text.as_bytes() {
            if x + 8 > self.width { break; }
            x = self.draw_char(x, y, ch, fg, bg);
        }
        x
    }

    /// Draw a formatted string using a temporary stack buffer.
    pub fn draw_fmt(&mut self, x: usize, y: usize,
                    fg: (u8, u8, u8), bg: (u8, u8, u8),
                    args: fmt::Arguments<'_>) -> usize {
        use core::fmt::Write;
        let mut buf = FmtBuf::new();
        let _ = buf.write_fmt(args);
        self.draw_str(x, y, buf.as_str(), fg, bg)
    }

    /// Scroll the framebuffer up by `lines` character rows (each 16 px tall).
    pub fn scroll_up(&mut self, lines: usize,
                     (br, bg, bb): (u8, u8, u8)) {
        let row_bytes = self.stride * self.bytes_per_pixel;
        let px_lines  = lines * 16;
        if px_lines >= self.height { self.clear(br, bg, bb); return; }
        let src = px_lines * row_bytes;
        let len = (self.height - px_lines) * row_bytes;
        // Safety: source and destination are within the framebuffer
        unsafe {
            core::ptr::copy(self.ptr.add(src), self.ptr, len);
        }
        // Clear newly revealed bottom rows
        self.fill_rect(0, self.height - px_lines, self.width, px_lines, br, bg, bb);
    }
}

// ── Small fmt helper ──────────────────────────────────────────────────────────
struct FmtBuf {
    buf: [u8; 256],
    pos: usize,
}
impl FmtBuf {
    fn new() -> Self { Self { buf: [0; 256], pos: 0 } }
    fn as_str(&self) -> &str {
        core::str::from_utf8(&self.buf[..self.pos]).unwrap_or("?")
    }
}
impl fmt::Write for FmtBuf {
    fn write_str(&mut self, s: &str) -> fmt::Result {
        for &b in s.as_bytes() {
            if self.pos < 255 { self.buf[self.pos] = b; self.pos += 1; }
        }
        Ok(())
    }
}

// ── Global instance ───────────────────────────────────────────────────────────
static FB: Once<Mutex<Framebuffer>> = Once::new();

/// Initialise from bootloader framebuffer data.
/// `ptr`   — virtual address of the framebuffer buffer.
/// `width` / `height` / `stride` — in pixels.
/// `bpp`   — bytes per pixel (3 or 4).
/// `fmt`   — pixel component order.
pub fn init(ptr: *mut u8, width: usize, height: usize,
            stride: usize, bpp: usize, fmt: PixelFormat) {
    FB.call_once(|| {
        Mutex::new(Framebuffer {
            ptr,
            width,
            height,
            stride,
            bytes_per_pixel: bpp,
            format: fmt,
        })
    });
    crate::klog!(INFO, "Framebuffer: {}×{} {}bpp stride={}", width, height, bpp * 8, stride);
}

/// Returns `true` if the framebuffer has been initialised.
pub fn is_available() -> bool { FB.get().is_some() }

/// Run a closure with mutable access to the global framebuffer.
/// Does nothing if the framebuffer is not yet available.
pub fn with<F: FnOnce(&mut Framebuffer)>(f: F) {
    if let Some(fb) = FB.get() {
        f(&mut fb.lock());
    }
}

/// Convenience wrappers forwarded to the global framebuffer.
pub fn clear(r: u8, g: u8, b: u8) {
    with(|fb| fb.clear(r, g, b));
}
pub fn fill_rect(x: usize, y: usize, w: usize, h: usize, r: u8, g: u8, b: u8) {
    with(|fb| fb.fill_rect(x, y, w, h, r, g, b));
}
pub fn draw_str(x: usize, y: usize, text: &str, fg: (u8,u8,u8), bg: (u8,u8,u8)) -> usize {
    let mut out = x;
    with(|fb| { out = fb.draw_str(x, y, text, fg, bg); });
    out
}
pub fn width()  -> usize { FB.get().map(|fb| fb.lock().width).unwrap_or(0) }
pub fn height() -> usize { FB.get().map(|fb| fb.lock().height).unwrap_or(0) }

/// Return the 8×16 bitmap glyph for an ASCII character (public helper for other modules).
pub fn glyph(ch: u8) -> &'static [u8; 16] { FONT_8X16.glyph(ch) }

// ═══════════════════════════════════════════════════════════════════════
// 8×16 bitmap font (PC VGA Code Page 437 subset, 128 glyphs)
// Data stored as 16 bytes per glyph (one byte = one scanline, MSB = leftmost pixel)
// ═══════════════════════════════════════════════════════════════════════
struct Font8x16;

impl Font8x16 {
    fn glyph(&self, ch: u8) -> &'static [u8; 16] {
        // Mask to 128 valid glyphs; chars >= 0x80 mirror lower half
        let idx = (ch as usize) & 0x7F;
        // Safety: FONT_DATA is 128*16 = 2048 bytes, idx is always < 128
        unsafe { &*(FONT_DATA.as_ptr().add(idx * 16) as *const [u8; 16]) }
    }
}
static FONT_8X16: Font8x16 = Font8x16;

