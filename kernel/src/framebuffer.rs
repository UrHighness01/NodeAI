//! Linear framebuffer abstraction — Phase 12a.
//!
//! Supports two back-ends:
//!   1. Bootloader-provided VESA/UEFI GOP framebuffer (primary, always present in QEMU).
//!   2. VirtIO-GPU framebuffer (attached later by the VirtIO-GPU driver).
//!
//! Public surface:
//!   - `init(info, buf_ptr)` — called once from `kernel_main` with BootInfo data.
//!   - `put_pixel(x, y, r, g, b)` — plot one pixel.
//!   - `fill_rect(x, y, w, h, r, g, b)` — solid coloured rectangle.
//!   - `blit(x, y, w, h, pixels)` — copy a raw BGRA pixel array.
//!   - `clear(r, g, b)` — fill entire screen with a colour.
//!   - `draw_str(x, y, text, fg, bg)` — render ASCII text with the built-in 8×16 font.
//!   - `draw_fmt(x, y, fg, bg, args)` — render a formatted string.
//!   - `width() / height()` — screen dimensions.
//!   - `flush()` — no-op for CPU-written framebuffers; VirtIO-GPU driver overrides.

